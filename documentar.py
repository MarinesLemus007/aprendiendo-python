def suma (numero_1, numero_2):

    """
    La función suma 2 números enteros.

    Argumentos:
    numero_1
    numero_2

    se rotorna la suma de los parámetros

    >>> suma(10,20)
    30

    >>> suma(100,200)
    300

    """
    return numero_1 + numero_2